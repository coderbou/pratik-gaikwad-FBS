class FactorialNo 
{
  public static void main(String[] args)
    {
       int no =5; 
       int fact = 1;
       int i = 1;

       while(i <= no)
       {
            
             fact = fact * i;
             i++;
        }

         System.out.println(fact);

    }//main methods end here

}//class FactorialNo ends here